#!/bin/bash
# ─────────────────────────────────────────────────────────────────
#  AgoraIQ — site-wide mobile responsive deploy
#  • Creates /opt/agoraiq-signals/web/assets/agoraiq-mobile.{css,js}
#  • Patches every *.html in /opt/agoraiq-signals/web/ to:
#     - Use optimized Google Fonts (preconnect + Syne display=block)
#     - Include the shared mobile CSS + JS
#     - Have a proper viewport meta tag
#  • Idempotent: safe to re-run.
#  • Backs up everything before touching it.
# ─────────────────────────────────────────────────────────────────

set -euo pipefail

WEB_DIR="/opt/agoraiq-signals/web"
ASSETS_DIR="$WEB_DIR/assets"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
BACKUP_DIR="$WEB_DIR/.mobile-patch-backup-$TIMESTAMP"

[ -d "$WEB_DIR" ] || { echo "ERROR: $WEB_DIR does not exist"; exit 1; }
mkdir -p "$ASSETS_DIR" "$BACKUP_DIR"

echo "▸ Backup dir: $BACKUP_DIR"
cp "$WEB_DIR"/*.html "$BACKUP_DIR/" 2>/dev/null || true

# ─────────────────────────────────────────────────────────────────
# 1. Shared mobile CSS
# ─────────────────────────────────────────────────────────────────
cat > "$ASSETS_DIR/agoraiq-mobile.css" << 'CSS_EOF'
